// Build flat lookup: any car number → { type, set, meta }
const FLEET_LOOKUP = {}
for (const [type, sets] of Object.entries(FLEET_SETS)) {
  const meta = FLEET_META[type]
  for (const set of sets) {
    for (const car of set) {
      FLEET_LOOKUP[car] = { type, set, meta }
    }
  }
}

export function getFleetInfo(vehicleId) {
  if (!vehicleId) return null
  // vehicleId may be a full consist string like "1325T-1339T-49M-50M-77M-78M"
  const cars = vehicleId.split('-').filter(Boolean)
  for (const car of cars) {
    const match = FLEET_LOOKUP[car]
    if (match) {
      return {
        type: match.type,
        meta: match.meta,
        consist: cars, // actual cars from the ID, not just one set
      }
    }
  }
  return null
}
